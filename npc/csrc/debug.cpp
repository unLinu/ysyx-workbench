extern "C" void npc_trap(uint32_t pc) {
    printf(ANSI_FMT("TRAP at PC = %#x", CYAN), pc);
    if (pc == 0x800001c8) {
        printf(ANSI_FMT("TEST SUCCESS!", GREEN));
        exit_flag = 1;
    } else {
        printf(ANSI_FMT("TEST FAILED!", RED));
        exit_flag = 1;
    }
}

uint32_t pmem_read(uint32_t addr) {
    assert(addr % 4 == 0);
    if (addr < 0x80000000 || addr >= 0x80000000 + sizeof(inst_mem)) {
        printf(ANSI_FMT("pmem_read out of bound addr = %#x", RED), addr);
        assert(0 && "pmem_read out of bound");
    }
    return inst_mem[(addr - 0x80000000) / 4];
}
